import random


def guess_number_game():
    """–û—Å–Ω–æ–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –∏–≥—Ä—ã"""

    print("=" * 40)
    print("–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ –∏–≥—Ä—É '–£–≥–∞–¥–∞–π –ß–∏—Å–ª–æ'!")
    print("–ö–æ–º–ø—å—é—Ç–µ—Ä –∑–∞–≥–∞–¥–∞–ª —á–∏—Å–ª–æ –æ—Ç 1 –¥–æ 20.")
    print("–ü–æ–ø—Ä–æ–±—É–π—Ç–µ —É–≥–∞–¥–∞—Ç—å –µ–≥–æ –∑–∞ –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–ø—ã—Ç–æ–∫!")
    print("=" * 40)

    while True:  # –ì–ª–∞–≤–Ω—ã–π —Ü–∏–∫–ª –∏–≥—Ä—ã
        # –ö–æ–º–ø—å—é—Ç–µ—Ä –∑–∞–≥–∞–¥—ã–≤–∞–µ—Ç —á–∏—Å–ª–æ
        secret_number = random.randint(1, 20)
        attempts = 0  # –°—á–µ—Ç—á–∏–∫ –ø–æ–ø—ã—Ç–æ–∫

        print(f"\nüéÆ –ù–æ–≤—ã–π —Ä–∞—É–Ω–¥! –Ø –∑–∞–≥–∞–¥–∞–ª —á–∏—Å–ª–æ –æ—Ç 1 –¥–æ 20.")

        while True:  # –¶–∏–∫–ª –¥–ª—è –æ–¥–Ω–æ–π –∏–≥—Ä—ã
            try:
                # –ü–æ–ª—É—á–∞–µ–º –¥–æ–≥–∞–¥–∫—É –æ—Ç –∏–≥—Ä–æ–∫–∞
                guess = int(input("\n–í–∞—à–∞ –¥–æ–≥–∞–¥–∫–∞: "))
                attempts += 1

                if guess < 1 or guess > 20:
                    print("‚ö†Ô∏è –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ –æ—Ç 1 –¥–æ 20!")
                    continue

                # –ü—Ä–æ–≤–µ—Ä—è–µ–º –¥–æ–≥–∞–¥–∫—É
                if guess < secret_number:
                    print("‚¨ÜÔ∏è  –ë–æ–ª—å—à–µ! –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â–µ —Ä–∞–∑.")
                elif guess > secret_number:
                    print("‚¨áÔ∏è  –ú–µ–Ω—å—à–µ! –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â–µ —Ä–∞–∑.")
                else:
                    # –ò–≥—Ä–æ–∫ —É–≥–∞–¥–∞–ª —á–∏—Å–ª–æ
                    print(f"üéâ –ü–æ–∑–¥—Ä–∞–≤–ª—è–µ–º! –í—ã —É–≥–∞–¥–∞–ª–∏ —á–∏—Å–ª–æ {secret_number}!")
                    print(f"üìä –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–ø—ã—Ç–æ–∫: {attempts}")
                    break  # –í—ã—Ö–æ–¥–∏–º –∏–∑ —Ü–∏–∫–ª–∞ –∏–≥—Ä—ã

            except ValueError:
                print("‚ùå –û—à–∏–±–∫–∞! –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏—Ç–µ —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ.")

        # –°–ø—Ä–∞—à–∏–≤–∞–µ–º, —Ö–æ—á–µ—Ç –ª–∏ –∏–≥—Ä–æ–∫ —Å—ã–≥—Ä–∞—Ç—å –µ—â–µ —Ä–∞–∑
        while True:
            play_again = input("\n–•–æ—Ç–∏—Ç–µ —Å—ã–≥—Ä–∞—Ç—å –µ—â–µ —Ä–∞–∑? (–¥–∞/–Ω–µ—Ç): ").lower().strip()
            if play_again in ['–¥–∞', '–¥', 'yes', 'y']:
                print("\n" + "=" * 40)
                break  # –ù–∞—á–∏–Ω–∞–µ–º –Ω–æ–≤—É—é –∏–≥—Ä—É
            elif play_again in ['–Ω–µ—Ç', '–Ω', 'no', 'n']:
                print("\n–°–ø–∞—Å–∏–±–æ –∑–∞ –∏–≥—Ä—É! –î–æ –Ω–æ–≤—ã—Ö –≤—Å—Ç—Ä–µ—á! üéÆ")
                print("=" * 40)
                return  # –ó–∞–≤–µ—Ä—à–∞–µ–º –ø—Ä–æ–≥—Ä–∞–º–º—É
            else:
                print("‚ùå –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –æ—Ç–≤–µ—Ç—å—Ç–µ '–¥–∞' –∏–ª–∏ '–Ω–µ—Ç'.")


# –ó–∞–ø—É—Å–∫–∞–µ–º –∏–≥—Ä—É
if __name__ == "__main__":
    guess_number_game()