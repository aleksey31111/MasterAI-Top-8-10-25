            raise ValueError(f"–ù–∞–±–æ—Ä —Å–∏–º–≤–æ–ª–æ–≤ '{set_names[i]}' –ø—É—Å—Ç –ø–æ—Å–ª–µ —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏–∏")

