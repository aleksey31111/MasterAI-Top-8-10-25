### 1
##–°–æ–∑–¥–∞—Ç—å —Ñ—É–Ω–∫—Ü–∏—é analyze_grades –Ω–∞ Python,
## –ö–æ—Ç–æ—Ä–∞—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç —Ç—Ä–∏ –∞—Ä–≥—É–º–µ–Ω—Ç–∞: –ò–º—è –°—Ç—É–¥–µ–Ω—Ç–∞ –∏ –ï–≥–æ –û—Ü–µ–Ω–∫–∏
## –í–µ—Ä–Ω—É—Ç—å —Å–ª–æ–≤–∞—Ä—å —Å —Å—Ä–µ–¥–Ω–∏–º –±–∞–ª–ª–æ–º, –º–∞–∫—Å –∏ –º–∏–Ω –æ—Ü–µ–Ω–∫–æ–π, –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä–µ–¥–º–µ—Ç–æ–≤.
def analyze_grades(student_name, *grades):
    """
    –ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ—Ç –æ—Ü–µ–Ω–∫–∏ —Å—Ç—É–¥–µ–Ω—Ç–∞.

    Parameters:
    -----------
    student_name : str
        –ò–º—è —Å—Ç—É–¥–µ–Ω—Ç–∞
    *grades : float or int
        –û—Ü–µ–Ω–∫–∏ —Å—Ç—É–¥–µ–Ω—Ç–∞ (–ø—Ä–æ–∏–∑–≤–æ–ª—å–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ)

    Returns:
    --------
    dict
        –°–ª–æ–≤–∞—Ä—å —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞–º–∏ –∞–Ω–∞–ª–∏–∑–∞:
        - –∏–º—è: –∏–º—è —Å—Ç—É–¥–µ–Ω—Ç–∞
        - —Å—Ä–µ–¥–Ω–∏–π_–±–∞–ª–ª: —Å—Ä–µ–¥–Ω—è—è –æ—Ü–µ–Ω–∫–∞
        - –º–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è_–æ—Ü–µ–Ω–∫–∞: –Ω–∞–∏–≤—ã—Å—à–∞—è –æ—Ü–µ–Ω–∫–∞
        - –º–∏–Ω–∏–º–∞–ª—å–Ω–∞—è_–æ—Ü–µ–Ω–∫–∞: –Ω–∞–∏–º–µ–Ω—å—à–∞—è –æ—Ü–µ–Ω–∫–∞
        - –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ_–ø—Ä–µ–¥–º–µ—Ç–æ–≤: –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –æ—Ü–µ–Ω–æ–∫

    Examples:
    ---------
    >>> analyze_grades("–ò–≤–∞–Ω", 5, 4, 3, 5, 4)
    {'–∏–º—è': '–ò–≤–∞–Ω', '—Å—Ä–µ–¥–Ω–∏–π_–±–∞–ª–ª': 4.2, '–º–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è_–æ—Ü–µ–Ω–∫–∞': 5,
     '–º–∏–Ω–∏–º–∞–ª—å–Ω–∞—è_–æ—Ü–µ–Ω–∫–∞': 3, '–∫–æ–ª–∏—á–µ—Å—Ç–≤–æ_–ø—Ä–µ–¥–º–µ—Ç–æ–≤': 5}
    """

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –æ—Ü–µ–Ω–∫–∏ –ø–µ—Ä–µ–¥–∞–Ω—ã
    if len(grades) == 0:
        raise ValueError("–ù–µ–æ–±—Ö–æ–¥–∏–º–æ –ø–µ—Ä–µ–¥–∞—Ç—å —Ö–æ—Ç—è –±—ã –æ–¥–Ω—É –æ—Ü–µ–Ω–∫—É")

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç—å –æ—Ü–µ–Ω–æ–∫
    for grade in grades:
        if not isinstance(grade, (int, float)):
            raise TypeError(f"–û—Ü–µ–Ω–∫–∞ –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å —á–∏—Å–ª–æ–º, –ø–æ–ª—É—á–µ–Ω–æ: {type(grade)}")
        if grade < 0 or grade > 10:  # –î–∏–∞–ø–∞–∑–æ–Ω 0-10, –º–æ–∂–Ω–æ –∏–∑–º–µ–Ω–∏—Ç—å
            raise ValueError(f"–û—Ü–µ–Ω–∫–∞ {grade} –≤–Ω–µ –¥–∏–∞–ø–∞–∑–æ–Ω–∞ 0-10")

    # –í—ã—á–∏—Å–ª—è–µ–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É
    max_grade = max(grades)
    min_grade = min(grades)
    count = len(grades)
    average = sum(grades) / count

    # –§–æ—Ä–º–∏—Ä—É–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
    result = {
        '–∏–º—è': student_name,
        '—Å—Ä–µ–¥–Ω–∏–π_–±–∞–ª–ª': round(average, 2),  # –û–∫—Ä—É–≥–ª—è–µ–º –¥–æ 2 –∑–Ω–∞–∫–æ–≤
        '–º–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è_–æ—Ü–µ–Ω–∫–∞': max_grade,
        '–º–∏–Ω–∏–º–∞–ª—å–Ω–∞—è_–æ—Ü–µ–Ω–∫–∞': min_grade,
        '–∫–æ–ª–∏—á–µ—Å—Ç–≤–æ_–ø—Ä–µ–¥–º–µ—Ç–æ–≤': count
    }

    return result


# –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã–≤–æ–¥–∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤ –≤ –∫—Ä–∞—Å–∏–≤–æ–º —Ñ–æ—Ä–º–∞—Ç–µ
def print_analysis(analysis_dict):
    """
    –ö—Ä–∞—Å–∏–≤–æ –≤—ã–≤–æ–¥–∏—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –∞–Ω–∞–ª–∏–∑–∞ –æ—Ü–µ–Ω–æ–∫.
    """
    print("\n" + "=" * 50)
    print(f"–ê–ù–ê–õ–ò–ó –£–°–ü–ï–í–ê–ï–ú–û–°–¢–ò: {analysis_dict['–∏–º—è']}")
    print("=" * 50)

    print(f"üìä –°—Ä–µ–¥–Ω–∏–π –±–∞–ª–ª: {analysis_dict['—Å—Ä–µ–¥–Ω–∏–π_–±–∞–ª–ª']}")
    print(f"‚≠ê –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è –æ—Ü–µ–Ω–∫–∞: {analysis_dict['–º–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è_–æ—Ü–µ–Ω–∫–∞']}")
    print(f"üìâ –ú–∏–Ω–∏–º–∞–ª—å–Ω–∞—è –æ—Ü–µ–Ω–∫–∞: {analysis_dict['–º–∏–Ω–∏–º–∞–ª—å–Ω–∞—è_–æ—Ü–µ–Ω–∫–∞']}")
    print(f"üìö –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—Ä–µ–¥–º–µ—Ç–æ–≤: {analysis_dict['–∫–æ–ª–∏—á–µ—Å—Ç–≤–æ_–ø—Ä–µ–¥–º–µ—Ç–æ–≤']}")

    # –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–∞—è –æ—Ü–µ–Ω–∫–∞ —É—Å–ø–µ–≤–∞–µ–º–æ—Å—Ç–∏
    avg = analysis_dict['—Å—Ä–µ–¥–Ω–∏–π_–±–∞–ª–ª']
    if avg >= 8.5:
        status = "–û—Ç–ª–∏—á–Ω–∞—è —É—Å–ø–µ–≤–∞–µ–º–æ—Å—Ç—å! üéì"
    elif avg >= 6.5:
        status = "–•–æ—Ä–æ—à–∞—è —É—Å–ø–µ–≤–∞–µ–º–æ—Å—Ç—å! üëç"
    elif avg >= 4.5:
        status = "–£–¥–æ–≤–ª–µ—Ç–≤–æ—Ä–∏—Ç–µ–ª—å–Ω–∞—è —É—Å–ø–µ–≤–∞–µ–º–æ—Å—Ç—å. üìö"
    else:
        status = "–¢—Ä–µ–±—É–µ—Ç —É–ª—É—á—à–µ–Ω–∏—è. üí™"

    print(f"\nüí° –°—Ç–∞—Ç—É—Å: {status}")
    print("=" * 50)


# –ü—Ä–∏–º–µ—Ä—ã –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è
if __name__ == "__main__":
    # –ü—Ä–∏–º–µ—Ä 1: –ë–∞–∑–æ–≤—ã–π –≤—ã–∑–æ–≤
    print("üìå –ü—Ä–∏–º–µ—Ä 1:")
    result1 = analyze_grades("–ú–∞—Ä–∏—è", 5, 4, 5, 3, 4, 5)
    print_analysis(result1)

