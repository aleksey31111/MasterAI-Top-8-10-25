    # –ö–ª–∞—Å—Å –¥–ª—è —É–¥–æ–±–Ω–æ–π —Ä–∞–±–æ—Ç—ã —Å —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞–º–∏
    class TemperatureConverter:
        """
        –ö–ª–∞—Å—Å –¥–ª—è –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–∏ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä –º–µ–∂–¥—É —Ä–∞–∑–Ω—ã–º–∏ —à–∫–∞–ª–∞–º–∏.

        –ü–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ–º—ã–µ —à–∫–∞–ª—ã:
        - Celsius (¬∞C)
        - Fahrenheit (¬∞F)
        - Kelvin (K)
        """

        # –ö–æ–Ω—Å—Ç–∞–Ω—Ç—ã –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä
        ABSOLUTE_ZERO_C = -273.15  # –ê–±—Å–æ–ª—é—Ç–Ω—ã–π –Ω–æ–ª—å –≤ –¶–µ–ª—å—Å–∏—è—Ö
        ABSOLUTE_ZERO_F = -459.67  # –ê–±—Å–æ–ª—é—Ç–Ω—ã–π –Ω–æ–ª—å –≤ –§–∞—Ä–µ–Ω–≥–µ–π—Ç–∞—Ö
        ABSOLUTE_ZERO_K = 0.0  # –ê–±—Å–æ–ª—é—Ç–Ω—ã–π –Ω–æ–ª—å –≤ –ö–µ–ª—å–≤–∏–Ω–∞—Ö

        @staticmethod
        def convert(value: float, from_scale: str, to_scale: str) -> float:
            """
            –ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ—Ç —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä—É –∏–∑ –æ–¥–Ω–æ–π —à–∫–∞–ª—ã –≤ –¥—Ä—É–≥—É—é.

            Parameters:
            -----------
            value : float
                –¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ –¥–ª—è –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–∏
            from_scale : str
                –ò—Å—Ö–æ–¥–Ω–∞—è —à–∫–∞–ª–∞: 'C', 'F' –∏–ª–∏ 'K'
            to_scale : str
                –¶–µ–ª–µ–≤–∞—è —à–∫–∞–ª–∞: 'C', 'F' –∏–ª–∏ 'K'

            Returns:
            --------
            float
                –ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω–∞—è —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞, –æ–∫—Ä—É–≥–ª–µ–Ω–Ω–∞—è –¥–æ 2 –∑–Ω–∞–∫–æ–≤

            Examples:
            ---------
            >>> TemperatureConverter.convert(0, 'C', 'F')
            32.0
            >>> TemperatureConverter.convert(100, 'C', 'K')
            373.15
            """
            from_scale = from_scale.upper()
            to_scale = to_scale.upper()

            # –ü—Ä–æ–≤–µ—Ä–∫–∞ –≤–∞–ª–∏–¥–Ω–æ—Å—Ç–∏ —à–∫–∞–ª
            valid_scales = {'C', 'F', 'K'}
            if from_scale not in valid_scales:
                raise ValueError(f"–ù–µ–ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ–º–∞—è –∏—Å—Ö–æ–¥–Ω–∞—è —à–∫–∞–ª–∞: {from_scale}. "
                                 f"–î–æ—Å—Ç—É–ø–Ω—ã–µ: {', '.join(valid_scales)}")
            if to_scale not in valid_scales:
                raise ValueError(f"–ù–µ–ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ–º–∞—è —Ü–µ–ª–µ–≤–∞—è —à–∫–∞–ª–∞: {to_scale}. "
                                 f"–î–æ—Å—Ç—É–ø–Ω—ã–µ: {', '.join(valid_scales)}")

            # –ï—Å–ª–∏ —à–∫–∞–ª—ã –æ–¥–∏–Ω–∞–∫–æ–≤—ã–µ, –ø—Ä–æ—Å—Ç–æ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ
            if from_scale == to_scale:
                return round(value, 2)

            # –ü—Ä–æ–≤–µ—Ä–∫–∞ –∞–±—Å–æ–ª—é—Ç–Ω–æ–≥–æ –Ω—É–ª—è
            TemperatureConverter._check_absolute_zero(value, from_scale)

            # –ú–∞—Ç—Ä–∏—Ü–∞ –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–π
            conversion_matrix = {
                ('C', 'F'): celsius_to_fahrenheit,
                ('C', 'K'): celsius_to_kelvin,
                ('F', 'C'): fahrenheit_to_celsius,
                ('F', 'K'): fahrenheit_to_kelvin,
                ('K', 'C'): kelvin_to_celsius,
                ('K', 'F'): kelvin_to_fahrenheit,
            }

            # –í—ã–ø–æ–ª–Ω—è–µ–º –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏—é
            converter = conversion_matrix.get((from_scale, to_scale))
            if converter:
                return converter(value)
            else:
                # –ù–µ –¥–æ–ª–∂–Ω–æ —Å–ª—É—á–∏—Ç—å—Å—è, –Ω–æ –Ω–∞ –≤—Å—è–∫–∏–π —Å–ª—É—á–∞–π
                raise ValueError(f"–ö–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏—è –∏–∑ {from_scale} –≤ {to_scale} –Ω–µ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç—Å—è")

        @staticmethod
        def _check_absolute_zero(value: float, scale: str) -> None:
            """
            –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —á—Ç–æ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ –Ω–µ –Ω–∏–∂–µ –∞–±—Å–æ–ª—é—Ç–Ω–æ–≥–æ –Ω—É–ª—è.

            Parameters:
            -----------
            value : float
                –¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏
            scale : str
                –®–∫–∞–ª–∞ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä—ã

            Raises:
            -------
            ValueError
                –ï—Å–ª–∏ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ –Ω–∏–∂–µ –∞–±—Å–æ–ª—é—Ç–Ω–æ–≥–æ –Ω—É–ª—è
            """
            scale = scale.upper()

            if scale == 'C' and value < TemperatureConverter.ABSOLUTE_ZERO_C:
                raise ValueError(
                    f"–¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –Ω–∏–∂–µ –∞–±—Å–æ–ª—é—Ç–Ω–æ–≥–æ –Ω—É–ª—è! "
                    f"–ü–æ–ª—É—á–µ–Ω–æ: {value}¬∞C, –º–∏–Ω–∏–º–∞–ª—å–Ω–æ: {TemperatureConverter.ABSOLUTE_ZERO_C}¬∞C"
                )
            elif scale == 'F' and value < TemperatureConverter.ABSOLUTE_ZERO_F:
                raise ValueError(
                    f"–¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –Ω–∏–∂–µ –∞–±—Å–æ–ª—é—Ç–Ω–æ–≥–æ –Ω—É–ª—è! "
                    f"–ü–æ–ª—É—á–µ–Ω–æ: {value}¬∞F, –º–∏–Ω–∏–º–∞–ª—å–Ω–æ: {TemperatureConverter.ABSOLUTE_ZERO_F}¬∞F"
                )
            elif scale == 'K' and value < TemperatureConverter.ABSOLUTE_ZERO_K:
                raise ValueError(
                    f"–¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ –Ω–µ –º–æ–∂–µ—Ç –±—ã—Ç—å –Ω–∏–∂–µ –∞–±—Å–æ–ª—é—Ç–Ω–æ–≥–æ –Ω—É–ª—è! "
                    f"–ü–æ–ª—É—á–µ–Ω–æ: {value}K, –º–∏–Ω–∏–º–∞–ª—å–Ω–æ: {TemperatureConverter.ABSOLUTE_ZERO_K}K"
                )

        @staticmethod
        def get_conversion_formula(from_scale: str, to_scale: str) -> str:
            """
            –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Ñ–æ—Ä–º—É–ª—É –¥–ª—è –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–∏ –º–µ–∂–¥—É —à–∫–∞–ª–∞–º–∏.

            Parameters:
            -----------
            from_scale : str
                –ò—Å—Ö–æ–¥–Ω–∞—è —à–∫–∞–ª–∞
            to_scale : str
                –¶–µ–ª–µ–≤–∞—è —à–∫–∞–ª–∞

            Returns:
            --------
            str
                –§–æ—Ä–º—É–ª–∞ –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–∏
            """
            formulas = {
                ('C', 'F'): "F = C √ó 9/5 + 32",
                ('C', 'K'): "K = C + 273.15",
                ('F', 'C'): "C = (F - 32) √ó 5/9",
                ('F', 'K'): "K = (F - 32) √ó 5/9 + 273.15",
                ('K', 'C'): "C = K - 273.15",
                ('K', 'F'): "F = (K - 273.15) √ó 9/5 + 32",
            }

            from_scale = from_scale.upper()
            to_scale = to_scale.upper()

            formula = formulas.get((from_scale, to_scale))
            if formula:
                return formula
            elif from_scale == to_scale:
                return "–®–∫–∞–ª—ã –æ–¥–∏–Ω–∞–∫–æ–≤—ã–µ, –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏—è –Ω–µ —Ç—Ä–µ–±—É–µ—Ç—Å—è"
            else:
                return "–§–æ—Ä–º—É–ª–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞"


    # –§—É–Ω–∫—Ü–∏—è –¥–ª—è —É–¥–æ–±–Ω–æ–≥–æ –≤—ã–≤–æ–¥–∞ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä
    def format_temperature(value: float, scale: str = "C") -> str:
        """
        –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä—É –¥–ª—è –∫—Ä–∞—Å–∏–≤–æ–≥–æ –≤—ã–≤–æ–¥–∞.

        Parameters:
        -----------
        value : float
            –¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞
        scale : str
            –®–∫–∞–ª–∞: 'C', 'F' –∏–ª–∏ 'K'

        Returns:
        --------
        str
            –û—Ç—Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω–∞—è —Å—Ç—Ä–æ–∫–∞ —Å —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–æ–π
        """
        scale = scale.upper()
        symbols = {'C': '¬∞C', 'F': '¬∞F', 'K': 'K'}

