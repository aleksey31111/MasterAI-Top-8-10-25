## –°–æ–∑–¥–∞–π—Ç–µ —Ñ—É–Ω–∫—Ü–∏—é greet(), –∫–æ—Ç–æ—Ä–∞—è:
# -–ü—Ä–∏–Ω–∏–º–∞–µ—Ç –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è (—Å—Ç—Ä–æ–∫–∞)
# -–ü—Ä–∏–Ω–∏–º–∞–µ—Ç –≤—Ä–µ–º—è —Å—É—Ç–æ–∫ (—Å—Ç—Ä–æ–∫–∞, –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é "–¥–µ–Ω—å")
# -–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—Ç—Ä–æ–∫—É –≤–∏–¥–∞: "–î–æ–±—Ä—ã–π [–≤—Ä–µ–º—è —Å—É—Ç–æ–∫], [–∏–º—è]
def greet(name: str, time_of_day: str = "–¥–µ–Ω—å") -> str:
    """
    –ü—Ä–∏–≤–µ—Ç—Å—Ç–≤—É–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å —É—á–µ—Ç–æ–º –≤—Ä–µ–º–µ–Ω–∏ —Å—É—Ç–æ–∫.

    Parameters:
    -----------
    name : str
        –ò–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    time_of_day : str, optional
        –í—Ä–µ–º—è —Å—É—Ç–æ–∫ (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é "–¥–µ–Ω—å")
        –ü–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ–º—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è: "—É—Ç—Ä–æ", "–¥–µ–Ω—å", "–≤–µ—á–µ—Ä", "–Ω–æ—á—å"

    Returns:
    --------
    str
        –ü—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ –≤ —Ñ–æ—Ä–º–∞—Ç–µ "–î–æ–±—Ä—ã–π [–≤—Ä–µ–º—è —Å—É—Ç–æ–∫], [–∏–º—è]!"

    Examples:
    ---------
    >>> greet("–ê–Ω–Ω–∞")
    '–î–æ–±—Ä—ã–π –¥–µ–Ω—å, –ê–Ω–Ω–∞!'

    >>> greet("–ò–≤–∞–Ω", "–≤–µ—á–µ—Ä")
    '–î–æ–±—Ä—ã–π –≤–µ—á–µ—Ä, –ò–≤–∞–Ω!'

    >>> greet("–ú–∞—Ä–∏—è", "—É—Ç—Ä–æ")
    '–î–æ–±—Ä–æ–µ —É—Ç—Ä–æ, –ú–∞—Ä–∏—è!'
    """
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ—Å—Ç—å –≤—Ä–µ–º–µ–Ω–∏ —Å—É—Ç–æ–∫
    valid_times = ["—É—Ç—Ä–æ", "–¥–µ–Ω—å", "–≤–µ—á–µ—Ä", "–Ω–æ—á—å"]

    # –ü—Ä–∏–≤–æ–¥–∏–º –∫ –Ω–∏–∂–Ω–µ–º—É —Ä–µ–≥–∏—Å—Ç—Ä—É –¥–ª—è —É–Ω–∏—Ñ–∏–∫–∞—Ü–∏–∏
    time_of_day_lower = time_of_day.lower()

    if time_of_day_lower not in valid_times:
        # –ï—Å–ª–∏ –≤—Ä–µ–º—è —Å—É—Ç–æ–∫ –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ, –∏—Å–ø–æ–ª—å–∑—É–µ–º –Ω–µ–π—Ç—Ä–∞–ª—å–Ω—ã–π –≤–∞—Ä–∏–∞–Ω—Ç
        return f"–ü—Ä–∏–≤–µ—Ç, {name}!"

    # –°–æ–≥–ª–∞—Å–æ–≤–∞–Ω–∏–µ –ø—Ä–∏–ª–∞–≥–∞—Ç–µ–ª—å–Ω–æ–≥–æ —Å —Ä–æ–¥–æ–º –≤—Ä–µ–º–µ–Ω–∏ —Å—É—Ç–æ–∫
    # "—É—Ç—Ä–æ" - —Å—Ä–µ–¥–Ω–∏–π —Ä–æ–¥, –ø–æ—ç—Ç–æ–º—É "–¥–æ–±—Ä–æ–µ"
    if time_of_day_lower == "—É—Ç—Ä–æ":
        greeting_word = "–î–æ–±—Ä–æ–µ"
    else:
        greeting_word = "–î–æ–±—Ä—ã–π"

    # –§–æ—Ä–º–∏—Ä—É–µ–º –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ
    greeting = f"{greeting_word} {time_of_day_lower}, {name}!"

    # –î–ª—è "–Ω–æ—á–∏" –º–æ–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –∞–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω—ã–π –≤–∞—Ä–∏–∞–Ω—Ç
    if time_of_day_lower == "–Ω–æ—á—å":
        # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –æ–±–∞ –≤–∞—Ä–∏–∞–Ω—Ç–∞
        alternative_greeting = f"{greeting}\n(–ò–ª–∏ –º–æ–∂–Ω–æ —Å–∫–∞–∑–∞—Ç—å: –°–ø–æ–∫–æ–π–Ω–æ–π –Ω–æ—á–∏, {name}!)"
        return alternative_greeting

    return greeting


