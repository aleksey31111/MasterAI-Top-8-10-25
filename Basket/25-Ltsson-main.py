    print("\n\nüìå –ü—Ä–∏–º–µ—Ä 2:")
    result2 = analyze_grades("–ê–ª–µ–∫—Å–µ–π", 4, 3, 4, 5, 2, 4, 3, 5)
    print_analysis(result2)

